function normalizeText(value) {
  return String(value || '').toLowerCase();
}

function scoreCategory(categoryItem, fileName, profile) {
  const fileNameValue = normalizeText(fileName);
  let score = 0;

  categoryItem.filenameHints.forEach((hint) => {
    if (fileNameValue.includes(hint)) {
      score += 8;
    }
  });

  if (profile.aspectRatio > 1.2 && categoryItem.category === 'Fashion Basics') {
    score += 7;
  }

  if (profile.brightness > 165 && categoryItem.category === 'Home Decor') {
    score += 4;
  }

  if (profile.saturation > 0.42 && ['Fashion Basics', 'Beauty Accessories'].includes(categoryItem.category)) {
    score += 6;
  }

  if (profile.saturation < 0.2 && ['Home Decor', 'Home Storage', 'Kitchen Accessories'].includes(categoryItem.category)) {
    score += 5;
  }

  if (profile.blueBias > 0.08 && categoryItem.category === 'Consumer Electronics') {
    score += 6;
  }

  if (profile.contrast > 28 && ['Consumer Electronics', 'Fashion Basics'].includes(categoryItem.category)) {
    score += 3;
  }

  return score;
}

function inferCatalogProfile(fileName, profile) {
  const enrichedProfile = profile || {
    brightness: 0,
    saturation: 0,
    blueBias: 0,
    contrast: 0,
    aspectRatio: 1,
  };

  let selectedCategory = CATEGORY_LIBRARY[0];
  let selectedScore = -Infinity;

  CATEGORY_LIBRARY.forEach((categoryItem) => {
    const currentScore = scoreCategory(categoryItem, fileName, enrichedProfile);
    if (currentScore > selectedScore) {
      selectedScore = currentScore;
      selectedCategory = categoryItem;
    }
  });

  const baseKeywords = selectedCategory.tags;
  const productName = `${selectedCategory.productStem} - ${selectedCategory.category}`;
  const tags = Array.from(
    new Set([
      ...baseKeywords,
      selectedCategory.category.toLowerCase(),
      selectedCategory.profitTier.toLowerCase(),
      selectedCategory.returnRisk.toLowerCase(),
    ])
  );

  const description = `Discover ${selectedCategory.productStem.toLowerCase()} for buyers who want ${selectedCategory.descriptionTone} catalogue listings. Built for value, search visibility, and strong Meesho conversion potential.`;

  return {
    category: selectedCategory.category,
    productName,
    description,
    tags,
    catalogProfile: selectedCategory,
  };
}

